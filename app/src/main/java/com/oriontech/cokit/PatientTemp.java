package com.oriontech.cokit;

public class PatientTemp {
    private String tempAm;
    private String tempNn;
    private String tempPm;

    public String getTempAm() {
        return tempAm;
    }

    public void setTempAm(String tempAm) {
        this.tempAm = tempAm;
    }

    public String getTempNn() {
        return tempNn;
    }

    public void setTempNn(String tempNn) {
        this.tempNn = tempNn;
    }

    public String getTempPm() {
        return tempPm;
    }

    public void setTempPm(String tempPm) {
        this.tempPm = tempPm;
    }
}
